<script type="text/javascript">
var base_url = '<?php echo base_url();?>weixin.php/';
function submitClick() {
	var join_name = $('#join_name').val();
	var join_phone = $('#join_phone').val();
	var join_address = $('#join_address').val();
	var join_info = $('#join_info').val();

	if (!join_name) {
		mui.toast('请输入姓名');
		return;
	}
	if (!join_phone) {
		mui.toast('请输入手机号');
		return;
	}
	var reg = /^((\(\d{2,3}\))|(\d{3}\-))?(13|14|15|16|17|18|19)\d{9}$/;
	if(!reg.test(join_phone)) {
		mui.toast('请输入正确的手机号');
		return;
	}
	if (!join_address) {
		mui.toast('请输入地址');
		return;
	}
	if (!join_info) {
		mui.toast('请选择类型');
		return;
	}
	var url = base_url + 'join/add';
	mui.ajax(url, {
		data: {
			name: join_name,
			mobile: join_phone,
			address: join_address,
			content: join_info
		},
		dataType: "json",
		type: "post",
		timeout: 10000, //超时时间设置为10秒；
		success: function(res) {
			console.log(JSON.stringify(res));
			if(res.success) {
				mui.toast('加盟资料已提交成功');
				$('#join_name').val('');
				$('#join_phone').val('');
				$('#join_adress').val('');
				$('#join_info').val('');
			} else {
				mui.toast(res.message);
			}
		},
		error: function(xhr, type, errorThrown) {
			if(type == 'timeout') {
				mui.toast('请求网络超时，请再次尝试');
			} else if(type == 'parsererror') {
				mui.toast('服务器返回数据格式错误');
			} else {
				mui.toast('请求网络失败:'+type);
			}
		}
	});
}
</script>