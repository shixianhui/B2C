//图片预加载
function loadImages(){
  var images = [];
  var imgarr = [];
  images.push(
    'images/weixin/tuiguang10-title.png',
    'images/weixin/tuiguang10-title.png'
  );
  var num = 0;
  for(var i = 0,len = images.length;i < len;i++){
		imgarr[i] = new Image();
		imgarr[i].src = images[i];
    imgarr[i].onload = function(){
      num++;
      $('#percent')[0].innerHTML = Math.round(num/len*100) + '%';
      if(num === len){
        $('.loading').fadeOut();
        $('.wrapper').fadeIn(function(){
          pageslider(); //执行swiper
        });
      }
		}

	}
}

function pageslider(){

    //slider
    var slider = $('.slider').swiper({
      direction: 'vertical',
  		loop: true,
  		//autoplay : 3000,
  		speed:300,
  		//pagination : '.slider_arrow',
      //calculateHeight:true,
  		resizeReInit : true,
  		paginationClickable: true,
  		simulateTouch:false,
  		autoplayDisableOnInteraction:false,

    });

    return slider;
}
